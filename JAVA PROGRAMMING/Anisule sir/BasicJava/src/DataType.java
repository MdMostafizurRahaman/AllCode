
public class DataType {
	public static void main(String[] args) {
		int i = 10;
		float f = 12.2f;
		//float datatype er jonno float data er sesh e f add krte hoi
		double d =122;
		char ch = 'a';
		short s = 1281 ;
		boolean b = true;
		
		System.out.println("Boolean:"+b);
		System.out.printf("Boolean:%b\n",b);
		//format specifier use krle % die use krte hbe
		//r printf function use krte hbe
		//ei khan e , die oi avriable er nam likhte hbe
		//+die hbe nah
		System.out.println("Integer:"+i);
		System.out.printf("Integer:%d\n",i);
		System.out.println("Float:"+f);
		System.out.printf("Float:%.1f\n",f);
		System.out.println("Short:"+s);
		System.out.printf("Short:%d\n",s);
		System.out.println("Character:" +ch);
		System.out.printf("Character:%c\n",ch);
		System.out.println("Double:"+d);
		System.out.printf("Double:%.1f\n",d);
		
		
	}
}
